"""
Алгоритм пузырьковой сортировки (Bubble Sort) —
это простой алгоритм сортировки, который работает путем последовательного сравнения
и обмена соседних элементов в массиве, если они находятся в неправильном порядке.
Процесс повторяется до тех пор, пока массив не будет отсортирован.
"""
arr = [64, 34, 25, 12, 22, 11, 90]
n = len(arr)

# Проходим по всем элементам массива
for i in range(n):
    # Последние i элементов уже отсортированы, их можно не проверять
    for j in range(0, n-i-1):
        # Если текущий элемент больше следующего, меняем их местами
        if arr[j] > arr[j+1]:
            arr[j], arr[j+1] = arr[j+1], arr[j]

print("Отсортированный массив:", arr)

def bubble_sort(arr):
    n = len(arr)
    # Проходим по всем элементам массива
    for i in range(n):
        # Последние i элементов уже отсортированы, их можно не проверять
        for j in range(0, n-i-1):
            # Если текущий элемент больше следующего, меняем их местами
            if arr[j] > arr[j+1]:
                arr[j], arr[j+1] = arr[j+1], arr[j]
    return arr

# Пример использования
arr = [64, 34, 25, 12, 22, 11, 90]
sorted_arr = bubble_sort(arr)

print("Отсортированный массив:", sorted_arr)
